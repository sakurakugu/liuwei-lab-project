/***********************************************************************
函数功能：延时子函数，13个指令周期1循环
***********************************************************************/
void Delay(unsigned int dtemp) 
{
	while(dtemp--);
}
/***********************************************************************
子函数功能：延时templ  ms，有中断则不准
***********************************************************************/
void Delay_nms (unsigned int inittempl)
{
	unsigned int	i;
	unsigned char	gtemp;
	/******************************************************************/
	gtemp=0;
	if(GIE==1)
	{
		gtemp=1;
		GIE=0;
	}
	for(i=0;i<inittempl;i++)
	{
		Delay(154);
		asm("clrwdt");
	}
	if(gtemp==1)	GIE=1;
}
